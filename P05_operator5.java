public class P05_operator5 {
    public static void main(String[] args) {
        int a = 2;
        int b = 3;

        //biwise and
        System.out.println("a&b = "+(a&b));

        //bitwise or
        System.out.println("a|b = "+(a|b));

        //biwise xor
        System.out.println("a^b = "+(a^b));

        //bitwise  not
        System.out.println("~a = "+(~a));  //gives complement
    }
}
